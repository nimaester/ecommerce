import Head from "next/head";
import { useQuery } from "urql";
import { PRODUCT_QUERY } from "../lib/query";

import { Container, Text, Flex, Grid, Box } from "@chakra-ui/react";
import Items from "../components/Items";

export default function Home() {
  //Fetch products from strapi

  const [result] = useQuery({
    query: PRODUCT_QUERY,
  });

  const { data, fetching, error } = result;

  if (fetching) return <p>Loading...</p>;
  if (error) return <p>Oh no... {error.message}</p>;
  const inventories = data.inventories.data;

  return (
    <Box backgroundColor='brand.100' pb='1rem'>
      <Head>
        <title>Ecommerce App</title>
        <link rel='icon' href='/favicon.ico' />
        <meta name='description' content='Generated by create next app' />
      </Head>
      <Container maxW='container.xl'>
        <Text
          fontSize={{
            sm: "2rem",
            md: "3rem",
            lg: "4rem",
            xl: "5rem",
            base: "2rem",
          }}
          textAlign='center'
        >
          Main Page
        </Text>
        <Grid
          style={{ overflowY: "auto", scrollbarGutter: "stable both-edges" }}
          gridTemplateColumns={{
            sm: "repeat(2, 1fr)",
            md: "repeat(3, 1fr)",
            lg: "repeat(4, 1fr)",
            xl: "repeat(5, 1fr)",
            base: "repeat(2, 1fr)",
          }}
          mt='5rem'
          mb='5rem'
        >
          {inventories.map((item) => (
            <Items key={item.attributes.slug} item={item.attributes} />
          ))}
        </Grid>
      </Container>
    </Box>
  );
}
